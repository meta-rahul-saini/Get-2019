
/**
 * class to represent origin of shape in co-ordinate space
 * 
 * @author rahul
 *
 */
public class Point {

	private float x = 0;
	private float y = 0;

	// getters and setters
	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
